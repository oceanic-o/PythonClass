x = 5
y = 4

# Bitwise AND operation
print("x & y =", x & y)

# Bitwise OR operation
print("x | y =", x | y)

# Bitwise XOR operation
print("x ^ y =", x ^ y)

#Bitwise NOT operation
print("~x =", ~x)

#Bitwise right shift operator
print("x >> 1 =", x >> 1)
print("y >> 1 =", y >> 1)


#Bitwise left shift operator
print("x << 1 =", x << 1)
print("y << 1 =", y << 1)